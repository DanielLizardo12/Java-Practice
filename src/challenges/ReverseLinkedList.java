package challenges;

import java.util.LinkedList;

/**
 * @author Daniel
 * @Date 27/03/2023
 */

public class ReverseLinkedList {

    public static LinkedList<Integer> reverse(LinkedList<Integer> linkedList) {

        if (linkedList == null) {
            return null;
        }

        if (linkedList.size() == 1) {
            return linkedList;
        }


        return linkedList;
    }

}
